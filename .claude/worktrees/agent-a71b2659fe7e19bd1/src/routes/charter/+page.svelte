		<h2 class="doc-title">The Working Society</h2>
		<h3 class="doc-subtitle">Charter</h3>
		<p class="doc-kicker">A Definition Held in Common by All Member Societies</p>

		<hr />

		<section>
			<h3>Preamble</h3>
			<p>
				There are moments in history that test the character of a nation, that demand more than
				ordinary times prepare anyone for. We believe we are living in such a moment. We do not
				take that lightly. We resolve to meet it together.
			</p>
			<p>
				The institutions of American democracy and Amercian society are in deep dysfunction. Some
				can be reformed and others cannot. The failure of institutions does not mean the failure of
				the nation. This nation is not its government. It is not its Congress, its courts, or its
				presidency. This nation is its people. The character and promise of this nation has never
				lived in its institutions or its political parties. Those things have always lived in the
				hearts and minds of its people.
			</p>
			<p>
				It's with that knowledge and in that spirit that we found Working Societies. Not as a
				political movement, not as an ideology, not as a statement of what we are against. As a
				commitment to what we are building. A Working Society exists wherever people choose
				agency over powerlessness and solidarity over isolation.
			</p>
			<p>
				This Charter defines what a Working Society is. It is held in common by all member
				societies. No single society may change it. It is our shared definition of ourselves.
			</p>
		</section>

		<hr />

		<section>
			<h3>Part One: Foundational Principles</h3>
			<p>
				These principles define what a Working Society is. They are not aspirations or
				political positions - they are requirements. A society that abandons these principles has
				ceased to be a Working Society.
			</p>
			<ol>
				<li>
					<h4>I. Human Dignity</h4>
					<p>
						We hold that human dignity is equal, sacred, and inviolable. It cannot be earned or lost,
						granted or revoked. You have it because you are human: wherever you were born, whatever
						you have done, whoever you are. This is foundational.
					</p>
				</li>
				<li>
					<h4>II. Self-Governance</h4>
					<p>
						We hold the human right to democracy to be inviolable. No authority is legitimate that
						does not flow from the genuine, informed consent of the people subject to it. Anyone who
						seeks governance authority for personal gain, status, or power is utterly and completely
						unfit to hold it.
					</p>
				</li>
				<li>
					<h4>III. Honest Inquiry</h4>
					<p>
						We commit to honest inquiry. The world is what it is regardless of what we wish it to be.
						We follow evidence wherever it leads, acknowledging that we may be wrong and that truth
						emerges through deliberation. We cultivate both intellectual humility and the courage to
						act on what we know.
					</p>
				</li>
				<li>
					<h4>IV. Mutual Obligation</h4>
					<p>
						Human dignity cannot exist in the absence of food, shelter, healthcare, and safety. We
						accept collective responsibility for ensuring every member has these essentials. This is
						mutual obligation, not charity - we guarantee to each other what dignity requires.
					</p>
				</li>
				<li>
					<h4>V. Stewardship</h4>
					<p>
						We are stewards of the natural world. We treat land, water, and living systems as held in
						trust for those who come after us. We reject degradation we cannot restore and
						consumption that leaves depletion. Future generations have the same right to a living
						world that we inherited.
					</p>
				</li>
				<li>
					<h4>VI. Clarity</h4>
					<p>
						We protect our capacity to think and decide for ourselves. We reject systems designed to
						manipulate perception, exploit attention, or engineer dependence. We build community life
						that supports independent thought and genuine connection rather than capturing and
						commodifying them.
					</p>
				</li>
				<li>
					<h4>VII. Accountability</h4>
					<p>
						We hold ourselves to genuine accountability: to each other, to our principles, and to all
						people. When harm is done or obligations are violated, we act swiftly to address it.
						Authority magnifies accountability rather than shielding from it.
					</p>
				</li>
				<li>
					<h4>VIII. Moral Economy</h4>
					<p>
						Human worth is measured by character and contribution, not by wealth or possessions. We
						reject the subordination of dignity and community to financial gain. Money is a tool in
						service of human flourishing, not its measure.
					</p>
				</li>
				<li>
					<h4>IX. Non-Domination</h4>
					<p>
						We reject domination. We hold as illegitimate the use force, coercion, or control of
						resources to subordinate another person or to claim authority not freely granted.
						Authority within this society flows from consent and is always recallable.
					</p>
				</li>
				<li>
					<h4>X. Transparency</h4>
					<p>
						We reject opacity. Institutions must be comprehensible to the people subject to them.
						Information about decisions, resources, and operations is held in common. Complexity
						shall not be used as a barrier to participation or a shield for power. All people have the
						right to understand what governs them.
					</p>
				</li>
			</ol>
		</section>

		<hr />

		<section>
			<h3>Part Two: What Every Working Society Must Have</h3>
			<p>
				A Working Society is not merely a community that holds the founding principles. It is
				a community that has built the institutions those principles require. The following are the
				institutional minimums: what a community must have and maintain to call itself a Ben
				Franklin Society. These are not aspirations. They are requirements.
			</p>
			<ol>
				<li>
					<h4>1. Community Authority</h4>
					<p>
						All authority originates with and belongs to the community in its entirety. A Ben Franklin
						Society must maintain this principle in its institutional design: not as a statement of
						belief but as an operational reality. Authority is delegated downward for practical
						purposes and is always recallable upward. The community has final say over all
						institutions, bodies, and decisions within the Society.
					</p>
				</li>
				<li>
					<h4>2. General Assembly</h4>
					<p>
						A Working Society must maintain a General Assembly drawn by sortition from the full
						adult membership. The General Assembly is the primary decision-making body of the Society,
						acting by delegation from the community. It must be genuinely sortition-drawn: no
						elections, no appointments, no hereditary positions. The randomness of selection is not a
						procedural detail. It is the mechanism by which the General Assembly remains the
						community's body rather than any faction's instrument.
					</p>
				</li>
				<li>
					<h4>3. The Frank</h4>
					<p>
						A Working Society must issue and maintain the Frank as its community currency. The
						Frank is issued on the basis of the person-year, equal for every member, and flows through
						the Treasury into the community's common life. The Frank is the material expression of the
						community's economic sovereignty. A Working Society that does not control its own
						currency has not achieved the economic sovereignty Principle V requires.
					</p>
				</li>
				<li>
					<h4>4. Financial Infrastructure</h4>
					<p>
						A Working Society must maintain the foundational financial institutions without which
						a Frank economy cannot function: a Central Bank responsible for Frank issuance and the
						membership roll; a Community Bank maintaining the ledger of Frank holdings and
						transactions; a Treasury receiving dues and disbursing funds per General Assembly
						appropriation; and a Social Insurance Fund providing regular support to elderly and
						disabled members. These four institutions are not optional. They are the infrastructure of
						economic self-governance.
					</p>
				</li>
				<li>
					<h4>5. The Colleges</h4>
					<p>
						A Working Society must maintain the Colleges framework: the system of peer bodies
						through which the community acknowledges domain expertise and draws qualified
						practitioners for governance in specialized domains. The specific Colleges a society
						maintains will vary with its size, population, and development. The framework itself (peer
						credentialing, qualified sortition, General Assembly creation and accountability) must be
						maintained. A Working Society governs itself through qualified democracy, not through
						either unqualified populism or expert technocracy.
					</p>
				</li>
				<li>
					<h4>6. Material Security</h4>
					<p>
						A Working Society must guarantee every member adequate food, healthcare, and shelter.
						No member shall go without these essentials through failure of the community to act. The
						Society must maintain health services at whatever level of capacity it can sustain,
						working continuously to expand that capacity. It must maintain food security through food
						services, reserves, and agricultural capacity development. It must ensure adequate shelter
						through housing services, emergency shelter, and maintenance support. The Society plans
						explicitly for circumstances in which conventional resources are scarce or absent.
						Resilience is built in, not assumed. The specific institutions may vary. The obligations do
						not.
					</p>
				</li>
				<li>
					<h4>7. Human Dignity in Practice</h4>
					<p>
						A Working Society must maintain institutional protections for human dignity that give
						Principle I operational force. No member shall be removed from membership, deprived of
						their rights, or subjected to institutional action without due process. No institution of
						the Society shall treat any member as less than fully human. The unconditional dignity of
						Principle I is not merely a statement of belief. It is a constitutional constraint on every
						institution the Society maintains.
					</p>
				</li>
				<li>
					<h4>8. Non-Partisan Infrastructure</h4>
					<p>
						A Working Society exists to serve all people in its community who accept its founding
						principles. It is infrastructure, not ideology. The Society may not be captured by any
						political party, factional interest, or ideological project. It does not exist to advance
						any agenda beyond its Charter obligations. Services, governance, and membership are not
						conditional on political alignment or ideological conformity. The Society protects its role
						as community infrastructure available to all who commit to its principles.
					</p>
				</li>
				<li>
					<h4>9. Fair Dealing with Neighboring Societies</h4>
					<p>
						A Working Society must deal fairly with other member societies. The economic
						sovereignty guaranteed to members within a society extends, in principle, to the relations
						between societies. A Working Society may not use control of essential resources
						(water, land access, transport routes, food supply, or infrastructure) to coerce, extract
						from, or systematically disadvantage another society. Trade and mutual aid between
						societies must be conducted in good faith. A society that leverages necessity into
						exploitation has violated the same principle it claims to uphold for its own members.
						Complaints of inter-society exploitation are subject to Federation mediation and may
						constitute grounds for compliance review.
					</p>
				</li>
				<li>
					<h4>10. Annual Review of Founding Purpose</h4>
					<p>
						A Working Society must convene an annual meeting dedicated to honest examination of
						whether the Society remains true to its founding purpose and principles. This is not a
						celebratory formality. It is a deliberate moment of institutional self-scrutiny. The
						meeting must engage these questions directly: Are we still serving the purpose we were
						founded to serve? Have our founding assumptions proven sound or do they require revision?
						Are our institutions achieving what they were designed to achieve? Where have we failed,
						and what must change? This annual review is not optional. It is the mechanism by which the
						Society maintains the honest inquiry required by Principle III and prevents institutional
						drift from charter obligations. The review findings must be made available to all members
						and must inform the General Assembly's legislative priorities for the following year.
					</p>
				</li>
				<li>
					<h4>11. Religious Liberty and Neutrality</h4>
					<p>
						A Working Society respects the religious conscience of every member and maintains
						strict neutrality in matters of faith. No member shall be required to profess, practice,
						or renounce any religious belief as a condition of membership, service, or governance. No
						religious test shall be applied to any position within the Society. The Society does not
						establish, endorse, or favor any religion or religious practice. It does not establish,
						endorse, or favor irreligion or opposition to religion. Members are free to hold and
						practice any faith or none. The Society's institutions, Services, and governance remain
						neutral ground where people of all faiths and none can work together toward common
						purposes.
					</p>
				</li>
				<li>
					<h4>12. Voluntary Association and Free Exit</h4>
					<p>
						Membership in a Working Society is entirely voluntary. No person may be compelled to
						join or remain a member. Members are free to leave at any time without penalty. A
						departing member may not be deprived of resources, compensation, or property to which they
						have a rightful claim. The Society may not use control of essential resources (healthcare
						access, housing, food security, or financial assets) to coerce retention of membership. A
						person who leaves in good standing retains any debts owed to them and may not be subjected
						to retaliation, blacklisting, or institutional punishment for their departure. The Society
						exists through the free choice of its members, not through dependence or coercion.
					</p>
				</li>
				<li>
					<h4>13. Aid Beyond Membership</h4>
					<p>
						The moral obligations of a Working Society extend beyond its membership. Where there
						is need outside the membership that the Society can reasonably address, the Society is
						obligated to provide aid. The Society does not exist to create exclusive security for
						members alone. It exists to demonstrate that communities can meet human needs through
						solidarity. A society that refuses to extend aid to those in need when it reasonably can do
						so violates the spirit of Principle IV (Mutual Obligation) and Principle I (Human
						Dignity). The Society balances its obligations to members with its moral duty to help
						others, but it does not hoard resources or close ranks when faced with need it has capacity
						to address.
					</p>
				</li>
				<li>
					<h4>14. Membership and Belonging</h4>
					<p>
						Membership in a Working Society is open to all people within the community who are
						willing to commit to its founding principles and contribute to its common life. A society
						may not impose barriers to membership beyond acceptance of the Charter principles and
						willingness to participate in the Society's mutual obligations. Membership cannot be
						conditional on wealth, social status, employment, education, or any other criterion that
						would create a privileged class. To become a provisional member, a person must be vouched
						for by three current members in good standing. After a minimum period defined by the
						Society's constitution, a provisional member may take the membership oath before witnesses
						to become a full member with all rights of participation, including eligibility for
						sortition. The Society defines its own membership age and residency requirements, but
						these must be applied uniformly. Membership can only be revoked through due process for
						violations of fundamental Charter principles or constitutional obligations. No member may
						be expelled for political views, religious belief, or personal disagreement so long as they
						uphold the Charter's principles in practice.
					</p>
				</li>
			</ol>
		</section>

		<hr />

		<section>
			<h3>Part Three: The Nature of This Charter</h3>
			<p>
				This Charter defines what a Working Society is. It is held in common by all member
				societies. No single society may change it. It is our shared definition of ourselves.
			</p>

			<h4>Held in Common</h4>
			<p>
				This Charter is held collectively by all Working Societies. It is not the property of
				any single society, any founding body, or any individual. It belongs to the network of
				communities that have adopted it and committed to living by it. No single society may amend
				it. Changes to the Charter require the deliberate consensus of the societies that hold it.
			</p>

			<h4>A Definition, Not a Constitution</h4>
			<p>
				This Charter defines what a Working Society is. It does not govern how any particular
				society governs itself. Each society maintains its own constitution: its own foundational
				document establishing the specific institutions, processes, and commitments by which it
				lives the Charter's principles and maintains the Charter's institutional minimums. A
				society's constitution may go further than this Charter requires. It may not fall short of
				it.
			</p>

			<h4>A Living Standard</h4>
			<p>
				The Charter is a living standard - a definition that communities are measured against, not
				just at founding but continuously. A Working Society that abandons its General
				Assembly, that surrenders its Frank, that allows its members to go without food or shelter
				or care, has ceased to be a Working Society in any meaningful sense. The Charter names
				what we are. Living up to it is the ongoing work.
			</p>

			<h4>Building Together</h4>
			<p>
				Benjamin Franklin built what his community needed. We build what ours needs. Every Ben
				Franklin Society, wherever it exists, is part of that same project - the ancient human
				project of neighbors deciding to take care of each other. We do not take that lightly. We
				resolve to meet it together.
			</p>
		</section>
